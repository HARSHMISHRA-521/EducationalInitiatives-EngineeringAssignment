package rover;

public class Obstacle {
    private int x;
    private int y;

    public Obstacle(int x, int y) {
        this.x = x;
        this.y = y;
    }


    /**
     * Gets the x-coordinate value.
     *
     * @return The x-coordinate value.
     */
    public int getX() {
        return x;
    }



    /**
     * Gets the y-coordinate value.
     *
     * @return The y-coordinate value.
     */
    public int getY() {
        return y;
    }
}
